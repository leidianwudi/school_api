    /** 根据$keyArr数组，删除$array数组内存一样key键的键值对，保持$array内原有key和value的对应关系不变
     * @param $array : 将被删除数据的原始数组
     * @param $keyArr: 要删除的key值数组 [$key1, $key2, $key3] 样式
