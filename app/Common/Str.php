    /**
     * @param $str:分割的字符串
     * @param int $l:每个元素长度
     * @return array|array[]|false|string[]:字符串按每个元素长度l分割成的数组
     */
    public static function splitUnicode($str, $l = 0)
    {
        if ($l > 0) {
            $ret = array();
            $len = mb_strlen($str, "UTF-8");
            for ($i = 0; $i < $len; $i += $l) {
                $ret[] = mb_substr($str, $i, $l, "UTF-8");
            }
            return $ret;
        }
        return preg_split("//u", $str, -1, PREG_SPLIT_NO_EMPTY);
    }

