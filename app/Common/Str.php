    /**
     * 替换字符串中给定值的第一个出现。
     * 把 $subject中的第一个$search替换成$replace
     * @param  string  $search 此字符串会被$replace替换
     * @param  string  $replace 用来替换的字符串
     * @param  string  $subject 完整字符串
     * @return string
     */
    public static function replaceFirst($search, $replace, $subject)
    {
        if ($search == '') {
            return $subject;
        }

        $position = mb_strpos($subject, $search);

        if ($position !== false) {
            return mb_substr($subject, 0, $position).$replace.mb_substr($subject, $position + mb_strlen($search));
        }

        return $subject;
    }

    /**
     * 替换字符串中给定值的最后出现。
     *
     * @param  string  $search 此字符串会被$replace替换
     * @param  string  $replace 用来替换的字符串
     * @param  string  $subject 完整字符串
     * @return string
     */
    public static function replaceLast($search, $replace, $subject)
    {
        $position = mb_strrpos($subject, $search);

        if ($position !== false) {
            return mb_substr($subject, 0, $position).$replace.mb_substr($subject, $position + mb_strlen($search));
        }

        return $subject;
    }

