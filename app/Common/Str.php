    /**
     * Returns the language specific replacements for the ascii method.
     *
     * Note: Adapted from Stringy\Stringy.
     *
     * @see https://github.com/danielstjules/Stringy/blob/3.1.0/LICENSE.txt
     *
     * @param  string  $language
     * @return array|null
     */
    protected static function languageSpecificCharsArray($language)
    {
        static $languageSpecific;

        if (! isset($languageSpecific)) {
            $languageSpecific = [
                'bg' => [
                    ['х', 'Х', 'щ', 'Щ', 'ъ', 'Ъ', 'ь', 'Ь'],
                    ['h', 'H', 'sht', 'SHT', 'a', 'А', 'y', 'Y'],
                ],
                'de' => [
                    ['ä',  'ö',  'ü',  'Ä',  'Ö',  'Ü'],
                    ['ae', 'oe', 'ue', 'AE', 'OE', 'UE'],
                ],
            ];
        }

        return $languageSpecific[$language] ?? null;
    }

    //把值转为可以识别的字符串
    public static function getStr($val): string
    {
        if (is_null($val)) return "null";
        else if(is_bool($val))
        {
            return $val ? "true" : "false";
        }
        else
            return $val;
    }

    //字符串是否是英文和数字组合
    public static function isCharNum($str): bool
    {
        $regex = "/^[a-zA-Z0-9_\s]+$/";
        return preg_match($regex, $str) ? true : false;
    }

    //字符串大小比较
    public static function strcmp($str1, $str2)
    {
        return strcmp($str1, $str2);
    }

    /**查找 $strFind 在 $strCom 中最后一次出现的位置
     * @param $strCom
     * @param $strFind
     * @return int
     */
    public static function lastPos($strCom, $strFind): int
    {
        $i = strripos($strCom, $strFind);
        if (Util::isNum($i)) return $i;
        return -1;
    }


